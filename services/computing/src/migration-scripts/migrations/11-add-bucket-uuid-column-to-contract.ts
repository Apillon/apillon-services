import { DbTables } from '../../config/types';

export async function upgrade(
  queryFn: (query: string, values?: any[]) => Promise<any[]>,
): Promise<void> {
  await queryFn(`
    ALTER TABLE \`${DbTables.CONTRACT}\`
      ADD bucket_uuid VARCHAR(45) NOT NULL AFTER project_uuid;
  `);
}

export async function downgrade(
  queryFn: (query: string, values?: any[]) => Promise<any[]>,
): Promise<void> {
  await queryFn(`
    ALTER TABLE \`${DbTables.CONTRACT}\`
    DROP
    COLUMN bucket_uuid;
  `);
}
