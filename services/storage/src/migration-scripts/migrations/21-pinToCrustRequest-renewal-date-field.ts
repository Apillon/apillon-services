import { DbTables } from '../../config/types';

export async function upgrade(
  queryFn: (query: string, values?: any[]) => Promise<any[]>,
): Promise<void> {
  await queryFn(`
    ALTER TABLE \`${DbTables.PIN_TO_CRUST_REQUEST}\`
    ADD COLUMN \`renewalDate\` DATETIME NULL;
    `);
}

export async function downgrade(
  queryFn: (query: string, values?: any[]) => Promise<any[]>,
): Promise<void> {
  await queryFn(`
        ALTER TABLE \`${DbTables.PIN_TO_CRUST_REQUEST}\` DROP COLUMN renewalDate;
    `);
}
