import { DbTables } from '../../config/types';

export async function upgrade(
  queryFn: (query: string, values?: any[]) => Promise<any[]>,
): Promise<void> {
  await queryFn(`
    ALTER TABLE \`${DbTables.TOKEN_CLAIM}\`
    ADD COLUMN \`transactionHash\` VARCHAR(300) NULL;
  `);
}

export async function downgrade(
  queryFn: (query: string, values?: any[]) => Promise<any[]>,
): Promise<void> {
  await queryFn(`
    ALTER TABLE \`${DbTables.TOKEN_CLAIM}\` DROP COLUMN \`transactionHash\`;
  `);
}
