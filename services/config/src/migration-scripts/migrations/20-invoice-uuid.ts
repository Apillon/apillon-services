import { DbTables } from '../../config/types';

export async function upgrade(
  queryFn: (query: string, values?: any[]) => Promise<any[]>,
): Promise<void> {
  await queryFn(`
    ALTER TABLE \`${DbTables.INVOICE}\`
    ADD COLUMN \`invoice_uuid\` VARCHAR(36) NULL AFTER \`project_uuid\`;
    `);
}

export async function downgrade(
  queryFn: (query: string, values?: any[]) => Promise<any[]>,
): Promise<void> {
  await queryFn(`
        ALTER TABLE \`${DbTables.INVOICE}\` DROP COLUMN \`invoice_uuid\`;
    `);
}
